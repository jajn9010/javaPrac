package ex04.For;

public class MainEntry {
	public static void main(String[] args) {
		
		for (int i = 1; i <= 10; i++) {
			System.out.println("DaeBo " + i);
		}
		for (int i = 1; i < 11; i++) {
			System.out.println("DaeBo " + i);
		}
		for (int i = 10; i >= 1; i--) {
			System.out.println("DaeBo " + i);
		}
		for (int i = 10; i > 0; i--) {
			System.out.println("DaeBo " + i);
		}
		for (int i = 1; i <= 5; i++) {
			System.out.print("*");
		}
		System.out.println();
		for (int i = 1; i <= 10; i++) {
			System.out.print(i + " ");
		}
		
		
	}
}
